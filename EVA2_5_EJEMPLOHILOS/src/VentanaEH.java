private void actualizarUI(){
        SwingUtilities.invokeLater(new Runnable(){
            @Override
            public void run() {
                int contarespacios = 0;
                if (txtarea.getText().length() == 1)
                        lblcaracteres.setText( (txtarea.getText().length() + " caracter"));
                    else
                       lblcaracteres.setText( (txtarea.getText().length() + " caracteres")); 

                for (int i = 0; i < txtarea.getText().length();i++)
                    if(txtarea.getText().charAt(i) == ' '){
                        contarespacios +=1;
                        if (contarespacios == 1)
                            lblespacios.setText(contarespacios + " espacio");
                        else
                            lblespacios.setText(contarespacios + " espacios");

                    }

            }

        });
    }

